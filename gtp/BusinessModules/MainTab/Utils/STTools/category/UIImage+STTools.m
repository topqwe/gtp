+ (UIImage *)st_imageWithColor:(UIColor *)color size:(CGSize)size
{
    CGRect rect = CGRectMake(0, 0, size.width, size.height);
    UIGraphicsBeginImageContext(rect.size);
    CGContextRef context = UIGraphicsGetCurrentContext();
    CGContextSetFillColorWithColor(context,color.CGColor);
    CGContextFillRect(context, rect);
    UIImage *img = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return img;
}
#pragma mark --生成二维码，高清和有色
+ (UIImage*)st_imageWithTwoCode:(NSString *)str andColorred:(CGFloat)red green:(CGFloat)green blue:(CGFloat)blue
{
    CIFilter *filter = [CIFilter filterWithName:@"CIQRCodeGenerator"];
    // 2.还原滤镜默认属性
    [filter setDefaults];
    // 3.设置需要生成二维码的数据到滤镜中
    // OC中要求设置的是一个二进制数据
    NSData *data = [str dataUsingEncoding:NSUTF8StringEncoding];
    [filter setValue:data forKeyPath:@"InputMessage"];
    
    // 4.从滤镜从取出生成好的二维码图片
    CIImage *ciImage = [filter outputImage];
    
    
    return [[self class] st_createNonInterpolatedUIImageFormCIImage:ciImage size: 500 andColorred:red green:green blue:blue];
}
+ (UIImage *)st_imageWithDefaultTwoCode:(NSString*)str
{
    return [self st_imageWithTwoCode:str andColorred:0 green:0 blue:0];
}
